    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        ModelTabeleKomponente mtk = (ModelTabeleKomponente) tblKomponente.getModel();
        int red = tblKomponente.getSelectedRow();
        if(red == -1){
            JOptionPane.showMessageDialog(this, "Morate da odaberete komponentu iz tabele");
            return;
        }
        Komponenta k = mtk.vratiKomponentu(red);
        Kontroler.getInstanca().obrisiKomponentu(k);
    }//GEN-LAST:event_btnObrisiActionPerformed

