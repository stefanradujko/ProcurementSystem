    private void srediTabeluInicijalno() {
        ModelTabeleKomponente mtk = new ModelTabeleKomponente();
        tblKomponente.setModel(mtk);
    }

    public void neuspesnoVracanjeKomponenti() {
        flag = false;
        JOptionPane.showMessageDialog(this, "Sistem ne može da pronađe komponente po zadatim parametrima");
    }

    public void vraceneKomponente(ArrayList<Komponenta> sveKomponente) {
        if(flag){
            JOptionPane.showMessageDialog(this, "Sistem je vratio sve komponente");
        }
        srediTabelu(sveKomponente);
    }

    private void srediTabelu(ArrayList<Komponenta> lista) {
        ModelTabeleKomponente mtk = new ModelTabeleKomponente(lista);
        tblKomponente.setModel(mtk);
        btnObrisi.setEnabled(true);
        btnPonisti.setEnabled(false);
        cekaj = false;
        flag = false;
    }

    private void vratiKomponenteUslov(String kriterijum) {
        ModelTabeleKomponente mtk = (ModelTabeleKomponente) tblKomponente.getModel();
        ArrayList<Komponenta> lista = mtk.filtriraj(kriterijum);
        cekaj = true;
        if(lista.isEmpty()){
            JOptionPane.showMessageDialog(this, "Sistem ne može da pronađe komponente po zadatim parametrima");
            return;
        }
        JOptionPane.showMessageDialog(this, "Sistem je pronašao komponente po zadatim parametrima");
        btnPonisti.setEnabled(true);
    }

    public void brisanjeKomponente(String poruka) {
        JOptionPane.showMessageDialog(this, poruka);
        txtFKriterijum.setText("");
        flag = true;
        Kontroler.getInstanca().vratiKomponente();
    }
