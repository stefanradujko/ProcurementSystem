    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        ModelTabeleProizvodjaci mtp = (ModelTabeleProizvodjaci) tblProizvodjaci.getModel();
        int red = tblProizvodjaci.getSelectedRow();
        if(red == -1){
            JOptionPane.showMessageDialog(this, "Morate da izaberete proizvođača u tabeli");
            return;
        }
        Proizvodjac p = mtp.vratiProizvodjaca(red);
        Kontroler.getInstanca().obrisiProizvodjaca(p);
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmenaActionPerformed
        if(txtFNaz.getText().isEmpty() && txtFEmail.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Morate popuniti bar jedno polje za izmenu");
            return;
        }
        ModelTabeleProizvodjaci mtp = (ModelTabeleProizvodjaci) tblProizvodjaci.getModel();
        int red = tblProizvodjaci.getSelectedRow();
        if(red == -1){
            JOptionPane.showMessageDialog(this, "Morate da izaberete proizvođača u tabeli");
            return;
        }
        Proizvodjac p = mtp.vratiProizvodjaca(red);
        String naz = txtFNaz.getText();
        if(!naz.isEmpty()){
            p.setNazivProizvodjaca(naz);
        }
        String email = txtFEmail.getText();
        if(!email.isEmpty()){
            if(!email.contains("@")){
            JOptionPane.showMessageDialog(this, "Email mora sadržati @");
            return;
            }
            if(email.startsWith("@") || email.endsWith("@")){
            JOptionPane.showMessageDialog(this, "Email ne sme da počinje ili da se završava sa @");
            return;
            }
            p.setEmailProizvodjaca(email);
        }
        Kontroler.getInstanca().izmeniProizvodjaca(p);
    }//GEN-LAST:event_btnIzmenaActionPerformed

