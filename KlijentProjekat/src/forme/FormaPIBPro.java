    public void neuspesnoVracanje() {
        JOptionPane.showMessageDialog(this, "Sistem ne može da pronađe proizvođače");
    }

    public void vraceniProizvodjaci(ArrayList<Proizvodjac> sviProizvodjaci) {
        JOptionPane.showMessageDialog(this, "Sistem je vratio sve proizvođače");
        btnPonisti.setEnabled(false);
        srediTabelu(sviProizvodjaci);
    }

    private void srediTabelu(ArrayList<Proizvodjac> lista) {
        ModelTabeleProizvodjaci mtp = new ModelTabeleProizvodjaci(lista);
        tblProizvodjaci.setModel(mtp);
        btnObrisi.setEnabled(true);
        btnIzmena.setEnabled(true);
    }

    private void vratiListuProZaUslov(String kriterijum) {
        Kontroler.getInstanca().vratiProizvodjaceZaUslov(kriterijum);
    }

    public void vraceniProizvodjaciZaUslov(ArrayList<Proizvodjac> listaProizvodjaca) {
        JOptionPane.showMessageDialog(this, "Sistem je pronašao proizvođače po zadatim vrednostima");
        srediTabelu(listaProizvodjaca);
    }

    private void srediTabeluInicijalno() {
        ModelTabeleProizvodjaci mtp = new ModelTabeleProizvodjaci();
        tblProizvodjaci.setModel(mtp);
    }

    public void brisanjeProizvodjaca(String poruka) {
        JOptionPane.showMessageDialog(this, poruka);
        vratiListuProizvodjaca();
    }

