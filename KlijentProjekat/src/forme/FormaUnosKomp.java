    public void neuspesnoVracanjeProizvodjaca() {
        JOptionPane.showMessageDialog(this, "Sistem ne može da vrati proizvođače");
    }

    public void vraceniProizvodjaci(ArrayList<Proizvodjac> sviProizvodjaci) {
        cBProizvodjac.removeAllItems();
        for(Proizvodjac p : sviProizvodjaci){
            cBProizvodjac.addItem(p);
        }
    }

    public void neuspesnoVracanjeTipova() {
        JOptionPane.showMessageDialog(this, "Sistem ne može da vrati tipove");
    }

    public void vraceniTipovi(ArrayList<TipKomponente> sviTipovi) {
        cBTip.removeAllItems();
        for(TipKomponente tk : sviTipovi){
            cBTip.addItem(tk);
        }
    }

    public void dodavanjeKomponente(String poruka) {
        JOptionPane.showMessageDialog(this, poruka);
        txtFCena.setText("");
        txtFGodina.setText("");
        txtFNaziv.setText("");
        txtAOpis.setText("");
        cBProizvodjac.setSelectedIndex(0);
        cBTip.setSelectedIndex(0);
        
    }
