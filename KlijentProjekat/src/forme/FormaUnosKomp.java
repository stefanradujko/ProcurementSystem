    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        if(txtFNaziv.getText().isEmpty() || txtFCena.getText().isEmpty() || txtFGodina.getText().isEmpty() || txtAOpis.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena");
            return;
        }
        String naziv = txtFNaziv.getText();
        double cena = Double.parseDouble(txtFCena.getText());
        if(cena < 0){
            JOptionPane.showMessageDialog(this, "Cena ne sme da bude negativna");
            return;
        }
        int godina = Integer.parseInt(txtFGodina.getText());
        if(godina < 2010 || godina > 2021){
            JOptionPane.showMessageDialog(this, "Godina proizvodnje mora biti u intervalu 2010 - 2021");
            return;
        }
        String opis = txtAOpis.getText();
        if(opis.length() > 100){
            JOptionPane.showMessageDialog(this, "Opis mora biti do 100 karaktera");
            return;
        }
        TipKomponente tk = (TipKomponente) cBTip.getSelectedItem();
        Proizvodjac pro = (Proizvodjac) cBProizvodjac.getSelectedItem();
        String stat = "Aktivan";
        Komponenta k = new Komponenta(0, naziv, cena, godina, opis, tk, pro, stat);
        Kontroler.getInstanca().dodajKomponentu(k);
    }//GEN-LAST:event_btnSacuvajActionPerformed

